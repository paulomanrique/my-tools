const fallback = translations.en

function buildLocale(locale: LocaleCode, overrides?: Partial<typeof fallback>) {
  const next = (overrides ?? {}) as typeof fallback
  return {
    ...fallback,
    ...next,
    youtube: { ...fallback.youtube, ...(next.youtube ?? {}) },
    image: { ...fallback.image, ...(next.image ?? {}) },
    dns: { ...fallback.dns, ...(next.dns ?? {}) },
    localeName: localeNames[locale],
  }
}

